						<h1>A* Search</h1>
						<p>
							A* search is an informed search using a provided heuristic. It is commonly used for graph traversal and pathfinding.
							It works on a directed, weighted graph, constantly expanding its frontier as a lowest-cost-first search, using the heuristic to estimate the remaining cost to reach the goal.
						</p>
						<p>
							The heuristic is problem specific. The algorithm is guaranteed to return the shortest path if the heuristic never provides an overestimate.
						</p>
						<p>
							In this project I have implemented a generic directed, weighted graph data structure to traverse. To use this algorithm for pathfinding, the world space should be represented as this type of graph.
							Furthermore, to choose the next node to explore I use a <i>min-heap</i> as the frontier. This makes acquiring the next node with the lowest cost cheap.
						</p>
