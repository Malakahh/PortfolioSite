						<h1>Heap</h1>


						<p>
							A heap is a tree-based structure. This heap is implemented as described in CLRS, and is a binary heap.	
						</p>
						<p>
							It has support to be two kinds of heap, a <i>max-heap</i> and a <i>min-heap</i>. Both satisfy the <i>heap property</i>.
							It follow that if we have an array <i>A</i>, the max-heap property is that for every node in A (except the root node) the parent is larger than both its children. In a min-heap, the parent node is smaller than both its children.
						</p>
						<p>
							This makes heaps a valuable data structure in several algorithms such as Dijkstra's algorithm or A* search.
						</p>

