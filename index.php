						<h1>Object Pool</h1>
						<p>
							The purpose of an object pool is essentially enabling recycling of objects, and it is done for performance reasons.
						</p>
						<p>
							The performance of the object pool has been benchmarked to be 42.08% faster than without using the object pool. The testing procedure were as follows: Instantiate/acquire a number of objects, and destroy/return every second one. Run 10 tests, and average the results.
						</p>
						<p>
							This object pool is developed in C# for Unity3D. It features async instantiation of any objects given a threshold. It will grow by N*2, where N = number of requests, ensuring a readily available supply of objects. Furthermore, it follows the Singleton pattern.
						</p>
						<p>
							Alongside the object pool, you will find unit tests as well as fully commented code and documentation to get started using the API.
						</p>

