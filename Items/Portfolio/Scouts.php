<h2>Description</h2>
<p>
	Chubby Chuck, Tina Ties-a-knot, and Billy Bullseye are scouts working together as a team.
	Each has a special ability: Chuck can use an axe, Tina a rope, and Billy a slingshot.
	Striving to earn badges, they travel through the forest encountering obstacles all solvable using their abilities.
	The player swipes to reposition the scouts, so the one with the correct ability meets the obstacle.
</p>
<p>
	Run, jump, use abilities, collect rare flowers and find rare animals to earn badges.
	If you keep your eyes open, you might even see the mythical Big-foot!
</p>
<p>
	SCOUTS! is an endless runner that offers whacky characters, funky badges, and epic scenery.
</p>
